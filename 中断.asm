SSTACK SEGMENT STACK
	DW 32 DUP(?)
SSTACK ENDS
CODE SEGMENT
	ASSUME CS:CODE
START: NOP
	PUSH DS
	MOV AX, 0000H
	MOV DS, AX
	MOV AX, OFFSET MIR7	;取中断入口地址
	MOV SI, 003CH
	MOV [SI], AX
	MOV AX, CS
	MOV SI, 003EH
	MOV [SI], AX
	CLI
	POP CS
	;---初始化8259
	MOV AL, 11H
	OUT 20H, AL	;ICW1
	MOV AL, 08H
	OUT 21H, AL	;ICW2
	MOV AL, 04H
	OUT 21H, AL	;ICW3
	MOV AL, 01H	;
	OUT 21H, AL	;ICW4
	
	MOV AL, 6FH	;OCW1
	OUT 21H, AL
	STI
AA1: NOP
	JMP AA1
	
MIR7 PROC
	PUSH AX
	STI
	;CALL DELAY
	MOV AX, 0137H
	INT 10H;
	MOV AX, 0120H
	INT 10H
	MOV AL, 20H
	OUT 20H, AL	;中断结束命令
	POP AX
	IRET
MIR7 ENDP
DELAY PROC
	PUSH CX
	MOV CX, 0F00H
AA0:PUSH AX
	POP AX
	LOOP AA0
	POP CX
	RET
DELAY ENDP
CODE ENDS
	END START

	
	