;定时器实验 -- 方式3的脉冲 控制开关灯
A8254 	EQU 0600H
B8254 	EQU 0602H
C8254 	EQU 0604H
CON8254 EQU 0606H
SSTACK SEGMENT STACK
	DW 32 DUP(?)
SSTACK ENDS
CODE SEGMENT
	ASSUME CS:CODE, SS:SSTACK
START:
	;用计数器1，在方式3下工作，按动5次--灯灭；再按动5次--灯亮
	MOV DX, CON8254
	MOV AL, 56H
	OUT DX, AL
	MOV DX, B8254
	MOV AL, 10		;送初值	
	OUT DX, AL
AA1:JMP AA1
CODE ENDS
	END START